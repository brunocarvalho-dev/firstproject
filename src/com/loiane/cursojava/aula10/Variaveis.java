package com.loiane.cursojava.aula10;

public class Variaveis {

	public static void main(String[] args) {
		
		
		String nome1 = "Bruno";
		
		byte bite = 127;
		short curto = 32767; 
		int idade = 2147483647;
		long idade5 = 9941665;
		char nomeDoMeuCachorro = 0x033; 
		boolean active = true;
		float flutuante = 32.00f;
		double conta = 64.00d;
		
		System.out.println("Nome = "+nome1);
		System.out.println("bite = "+ bite);
		System.out.println("short = "+ curto);
		System.out.println("Idade = "+idade);
		System.out.println("long = "+idade5);
		System.out.println("Cachorro = "+nomeDoMeuCachorro);
		System.out.println("valor double = "+active);
		System.out.println("Float = "+flutuante);
		System.out.println("R$" +conta);
		
		
		
		
		

	}

}
